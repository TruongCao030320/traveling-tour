import mongoose, { mongo } from "mongoose";
import { Schema } from "mongoose";
const reviewSchema = new Schema(
  {
    tourId: {
      type: mongoose.Types.ObjectId,
      ref: "Tour",
    },
    username: {
      type: String,
      required: true,
    },
    reviewText: {
      type: String,
      required: true,
    },
    rating: {
      type: Number,
      required: true,
      min: 0,
      max: 5,
      default: 0,
    },
  },
  { timestamps: true }
);
export default mongoose.model("Review", reviewSchema);
